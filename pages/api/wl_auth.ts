async function generateAddressSignature(address: string) {
  wl = wl.map(function (v) {
    return v.toLowerCase();
  });
  address = address.toLowerCase();
  if (!wl.includes(address)) {
    // TODO: show something on client that they are not on the whitelist
    console.log(address);
    return undefined;
  }

  const abiCoder = ethers.utils.defaultAbiCoder;
  console.log(address);

  const hash = ethers.utils.keccak256(abiCoder.encode(["address"], [address]));

  let private_key_here = "";
  if (process.env.PRIVATE_KEY) {
    private_key_here = process.env.PRIVATE_KEY;
  }
  const owner = new ethers.Wallet(private_key_here);
  return await owner.signMessage(ethers.utils.arrayify(hash));
}

export default async function handler(
  req: NextApiRequest,
  res: NextApiResponse<Data>
) {
  const sig = await generateAddressSignature(req.body["account"]);
  res.status(200).json({ sig: sig });
}
